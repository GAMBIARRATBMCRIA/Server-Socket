            /// Ligar ServidorClass
            classServidor.criaServerSocket(configsControleGlobal.portaServidor);

            areaSaida.append(textoProjeto.mensagemServidorIniciadoSucesso);
            label1Porta.setText(textoProjeto.label1Porta + configsControleGlobal.portaServidor);
            labelIP.setText(classServidor.ipLocal());
            labelHostname1.setText(classServidor.hostLocal());
