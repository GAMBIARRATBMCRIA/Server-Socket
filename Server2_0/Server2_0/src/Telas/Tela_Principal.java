    private void exibeStatusServer(String ligadoDeligado) {
        switch (ligadoDeligado) {
            case "L":
                imprimeStatusSever.setText(textoProjeto.label1StatusServer);
                imprimeStatusSever.setForeground(Color.WHITE);
                imprimeStatusSever.setBackground(new java.awt.Color(0, 216, 92));
                break;
            case "D":
                imprimeStatusSever.setText(textoProjeto.label1StatusServerDesligado);
                imprimeStatusSever.setForeground(Color.BLACK);
                imprimeStatusSever.setBackground(new java.awt.Color(168, 21, 14));
                break;
            default:
                throw new AssertionError();
        }

    }

